export interface Package {
  id: string
  name: string
  circuit: string
  location: string
  country: string
  countryCode: string
  date: string
  dateRange: string
  price: number | null
  currency: string
  availability: number | string
  totalCapacity: number
  image: string
  tier: "paddock" | "champions" | "legend" | "hero"
  includes: string[]
  featured?: boolean
  /** Set when loaded from Supabase (used for navigation) */
  raceId?: string
}

export interface Race {
  id: string
  name: string
  shortName: string
  location: string
  country: string
  countryCode: string
  date: string
  dateRange: string
  image: string
  packagesAvailable: number
  lowestPrice: number
}

export interface Booking {
  id: string
  /** Human-readable order reference from the database (e.g. ZK-2026-…) */
  orderReference?: string
  packageId: string
  packageName: string
  circuit: string
  date: string
  guests: number
  status: "confirmed" | "pending" | "cancelled"
  totalAmount: number
  currency: string
  createdAt: string
  clientName: string
  clientEmail: string
  /** When loaded from Supabase */
  packageTier?: string
}

export interface Invoice {
  id: string
  bookingId: string
  /** Internal order id for deep links from bookings */
  orderId?: string
  amount: number
  currency: string
  status: "paid" | "pending" | "overdue"
  issuedAt: string
  dueDate: string
  packageName: string
}

